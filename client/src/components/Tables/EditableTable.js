function Editable({row, col, table}) {
    console.log(row)
  
    const [columns, setColumns] = useState(col);
  
    const [data, setData] = useState([]);
    const handleDelete = async ()=>{
      const usr= await axios.delete(`http://localhost:4000/api/${table}/6016e621bac7ab34b4256d87`)
      console.log(usr)
    }

    
useEffect(() => {
  setData(row)
}, [row]);
    //console.log(data)  
    return (
      <MaterialTable
        title="Editable Preview"
        columns={columns}
        icons={tableIcons}
        data={data}
        editable={{
          onRowAdd: newData =>
            new Promise((resolve, reject) => {
              setTimeout(() => {
                setData([...data, newData]);
                ///call api to save user in mongodb

                const handleUpdate = async ()=>{
                  const usr= await axios.update(`http://localhost:4000/api/${table}/6016e621bac7ab34b4256d87`)
                  console.log(usr)
                }
               
                resolve();
              }, 1000)
            }),
          onRowUpdate: (newData, oldData) =>
            new Promise((resolve, reject) => {
              setTimeout(() => {
                const dataUpdate = [...data];
                const index = oldData.tableData.id;
                dataUpdate[index] = newData;
                setData([...dataUpdate]);
                ////call api of update
                // handleUpdate(); 
                resolve();
              }, 1000)
            }),
          onRowDelete: 
          oldData =>
            new Promise((resolve, reject) => {
              setTimeout(() => {
                const dataDelete = [...data];
                const index = oldData.tableData.id;
                dataDelete.splice(index, 1);
                setData([...dataDelete]);

                handleDelete();
                
                resolve()
              }, 1000)
            }),
        }}
      />
    )
  }
  export default Editable